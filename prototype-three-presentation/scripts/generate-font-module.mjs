import { readFile, mkdir, writeFile } from "node:fs/promises";
import path from "node:path";

const repositoryRoot = path.resolve(import.meta.dirname, "..", "..");
const sourceDirectory = path.join(repositoryRoot, "node_modules", "@fontsource", "inter", "files");
const outputDirectory = path.join(repositoryRoot, "prototype-three-presentation", "src", "generated");

async function dataUrl(filename) {
  const bytes = await readFile(path.join(sourceDirectory, filename));
  return `data:font/woff2;base64,${bytes.toString("base64")}`;
}

await mkdir(outputDirectory, { recursive: true });
const moduleSource = [
  "// GENERATED by scripts/generate-font-module.mjs from @fontsource/inter (OFL-1.1).",
  `export const inter400Url = ${JSON.stringify(await dataUrl("inter-latin-400-normal.woff2"))};`,
  `export const inter600Url = ${JSON.stringify(await dataUrl("inter-latin-600-normal.woff2"))};`,
  "",
].join("\n");
await writeFile(path.join(outputDirectory, "bundled-font-data.js"), moduleSource);
